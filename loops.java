public class loops {
    public static void main(String[] args) {
        // while loop----
        int i=0;
        while(i<=10)
        {
            System.out.println(i);
            i++;
            // ++i;
        }

        // do while loop----
        int b=0;
        do{
        System.out.println("Hello");
        b++;
        }while(b<=10);

        // for loop----
        
        for(int c=1;c<=10;c++)
        {
            System.out.println(c);
        }
    }
    
}
